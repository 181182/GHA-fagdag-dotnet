namespace Fagdag.FunctionApp;

/// <summary>
/// Provides basic calculator functions.
/// </summary>
public static class Kalkulator
{
    /// <summary>
    /// Sums two integers.
    /// </summary>
    /// <param name="a">The first integer to sum.</param>
    /// <param name="b">The second integer to sum.</param>
    /// <returns>The sum of the two integers.</returns>
    public static int Sum(int a, int b)
    {
        return a + b;
    }
}
